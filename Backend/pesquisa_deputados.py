import pandas as pd
from torch.nn.functional import cosine_similarity

def analisar_multiplos_parlamentares(lista_ids, qtd_discursos=1):
    consolidado = []

    for id_senador in lista_ids:
        print(f"\n>>> Analisando Parlamentar ID: {id_senador}...")

        # 1. Busca Discursos e Votos
        discursos = scrape_senado_discursos(id_senador, qtd=qtd_discursos)
        url_votos = f"https://legis.senado.leg.br/dadosabertos/senador/{id_senador}/votacoes"
        votos = extrair_e_filtrar_votos(url_votos)

        if not discursos or not votos:
            print(f"Dados insuficientes para o ID {id_senador}.")
            continue

        # 2. Embedding do Discurso mais recente
        texto_discurso = discursos[0]
        emb_discurso = get_embedding(texto_discurso[:512])

        # 3. Processamento de Afinidade
        for v in votos:
            emb_ementa = get_embedding(v['ementa'][:512])
            score = cosine_similarity(emb_discurso, emb_ementa).item()

            # Classificação
            status = "Coerente" if score >= 0.7 else "Parcialmente Alinhado" if score >= 0.6 else "Tema Divergente"

            consolidado.append({
                'ID Parlamentar': id_senador,
                'Data': v['data'],
                'Ementa': v['ementa'],
                'Voto': v['voto'],
                'Afinidade': round(score, 4),
                'Status': status
            })

    return pd.DataFrame(consolidado)

