class MonitoramentoLegislativo:
    def __init__(self):
        self.base_camara = "https://dadosabertos.camara.leg.br/api/v2"
        self.base_senado = "https://legis.senado.leg.br/dadosabertos"

    def get_deputados_influentes(self, nomes):
        """Busca IDs de deputados específicos na API da Câmara"""
        print(f"\n--- Buscando Deputados na Câmara ---")
        url = f"{self.base_camara}/deputados"
        params = {'ordem': 'ASC', 'ordenarPor': 'nome'}
        response = requests.get(url, params=params).json()

        deputados = [d for d in response['dados'] if d['nome'] in nomes]
        for d in deputados:
            print(f"ID: {d['id']} | Nome: {d['nome']} | Partido: {d['siglaPartido']}")
        return deputados

    def get_discursos_deputado(self, deputado_id):
        """Puxa os últimos discursos de um deputado"""
        url = f"{self.base_camara}/deputados/{deputado_id}/discursos"
        params = {'itens': 5} # Limitando para o terminal não poluir
        response = requests.get(url, params=params).json()
        return response['dados']

    def get_senadores_atuais(self, nomes):
        """Busca IDs de senadores específicos na API do Senado"""
        print(f"\n--- Buscando Senadores no Senado ---")
        url = f"{self.base_senado}/senador/lista/atual"
        headers = {'Accept': 'application/json'}
        response = requests.get(url, headers=headers).json()

        lista = response['ListaParlamentarEmExercicio']['Parlamentares']['Parlamentar']
        senadores = [s for s in lista if s['IdentificacaoParlamentar']['NomeParlamentar'] in nomes]

        for s in senadores:
            ident = s['IdentificacaoParlamentar']
            print(f"ID: {ident['CodigoParlamentar']} | Nome: {ident['NomeParlamentar']} | Partido: {ident['SiglaPartidoParlamentar']}")
        return senadores

