def extrair_e_filtrar_votos(url):
    """
    Busca o XML da URL, extrai aproximadamente 1/4 das votações
    e aplica a lógica de filtragem fornecida.
    """
    headers = {'Accept': 'application/xml'}
    try:
        response = requests.get(url, headers=headers)
        response.raise_for_status()

        # Parse do XML completo
        root = ET.fromstring(response.content)

        # Localiza todas as votações
        todas_votacoes = root.findall('.//Votacao')
        quantidade = len(todas_votacoes)

        # Pega o primeiro quarto das votações
        limite = max(1, quantidade // 4)
        votos_slice = todas_votacoes[:limite]

        print(f"Total de votações encontradas: {quantidade}")
        print(f"Processando as primeiras {limite} votações (1/4).")

        # Recria um XML parcial para a função de filtragem ou adapta a lógica
        # Para manter a compatibilidade exata com sua função, vamos passar o XML das selecionadas
        # Como a função espera uma string XML, vamos reconstruir um nó raiz temporário
        root_temp = ET.Element('ListaVotacoes')
        for v in votos_slice:
            root_temp.append(v)

        xml_parcial = ET.tostring(root_temp, encoding='unicode')
        return filtrar_votos(xml_parcial)

    except Exception as e:
        return f"Erro ao processar: {e}"

def filtrar_votos(xml_data):
    root = ET.fromstring(xml_data)
    votos_uteis = []

    for votacao in root.findall('.//Votacao'):
        # Ignora votações secretas
        voto_secreto = votacao.find('IndicadorVotacaoSecreta')
        if voto_secreto is not None and voto_secreto.text == 'Sim':
            continue

        ementa_node = votacao.find('.//Ementa')
        voto_node = votacao.find('SiglaDescricaoVoto')
        data_node = votacao.find('.//DataSessao')

        ementa = ementa_node.text if ementa_node is not None else "Sem ementa"
        voto = voto_node.text if voto_node is not None else "N/A"
        data = data_node.text if data_node is not None else "N/A"

        votos_uteis.append({
            "data": data,
            "ementa": ementa,
            "voto": voto
        })
    return votos_uteis

