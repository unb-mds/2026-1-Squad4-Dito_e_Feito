def debug_camara_discursos(deputado_id):
    url = f"https://dadosabertos.camara.leg.br/api/v2/deputados/{deputado_id}/discursos"
    params = {'itens': 5, 'ordem': 'DESC', 'ordenarPor': 'dataHoraInicio'}
    headers = {'Accept': 'application/json'}

    try:
        response = requests.get(url, params=params, headers=headers)
        data = response.json()

        if not data['dados']:
            print(f"Nenhum discurso encontrado para o ID {deputado_id}.")
            return

        for i, discurso in enumerate(data['dados']):
            print(f"\n--- Discurso {i+1} ---")
            print(f"Data: {discurso.get('dataHoraInicio')}")

            # Tentativa 1: Campo de transcrição direta
            texto = discurso.get('transcricao')

            # Tentativa 2: Algumas vezes a API retorna apenas palavras-chave
            keywords = discurso.get('keywords')

            # Tentativa 3: Verificar se há um link para o texto completo
            link_integra = discurso.get('urlTexto')

            if texto:
                print(f"Texto encontrado: {texto[:200]}...")
            elif keywords:
                print(f"ℹApenas palavras-chave disponíveis: {keywords}")

            if link_integra:
                print(f"Link para a íntegra: {link_integra}")

            if not texto and not keywords and not link_integra:
                print("Este registro existe, mas a API não forneceu conteúdo textual.")

    except Exception as e:
        print(f"Erro na requisição: {e}")

