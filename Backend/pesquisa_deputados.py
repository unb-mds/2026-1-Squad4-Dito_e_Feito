import requests
from bs4 import BeautifulSoup

def scrape_senado_discursos(id_senador, qtd=5):
    """Extrai os textos dos últimos 'qtd' discursos do senador."""
    url = f"https://www25.senado.leg.br/web/atividade/pronunciamentos/-/p/parlamentar/{id_senador}"
    headers = {'User-Agent': 'Mozilla/5.0'}

    try:
        response = requests.get(url, headers=headers, timeout=15)
        soup = BeautifulSoup(response.text, 'html.parser')
        tabela = soup.find('table')

        if not tabela: return []

        links_discursos = []
        for linha in tabela.find_all('tr')[1:qtd+1]:
            colunas = linha.find_all('td')
            if colunas:
                link = colunas[0].find('a')
                if link and 'href' in link.attrs:
                    url_d = link['href']
                    links_discursos.append("https://www25.senado.leg.br" + url_d if url_d.startswith('/') else url_d)

        textos = []
        for url_discurso in links_discursos:
            res_discurso = requests.get(url_discurso, headers=headers)
            soup_discurso = BeautifulSoup(res_discurso.text, 'html.parser')
            texto_div = soup_discurso.find('div', class_='texto-integral') or soup_discurso.find('div', id='textoPronunciamento')
            if texto_div:
                textos.append(texto_div.get_text(separator='\n').strip())

        return textos
    except Exception as e:
        print(f"Erro ao raspar: {e}")
        return []

from transformers import AutoTokenizer, AutoModel
import torch

# Carregamento do modelo pré-treinado (Base ou Large)
model_name = "neuralmind/bert-base-portuguese-cased"
tokenizer = AutoTokenizer.from_pretrained(model_name)
model = AutoModel.from_pretrained(model_name)

def get_embedding(text):
    # Tokenização e conversão para tensores
    inputs = tokenizer(text, return_tensors="pt", padding=True, truncation=True, max_length=512)

    with torch.no_grad():
        outputs = model(**inputs)

    # Utilizamos a média da última camada oculta como representação vetorial (embedding)
    embeddings = outputs.last_hidden_state.mean(dim=1)
    return embeddings

